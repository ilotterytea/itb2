package kz.ilotterytea.bot;

/**
 * Bot.
 * @author ilotterytea
 * @since 1.0
 */
abstract class Bot {
    /**
     * Initialize the bot.
     * @author ilotterytea
     * @since 1.0
     */
    abstract void init();
    /**
     * Dispose the bot.
     * @author ilotterytea
     * @since 1.0
     */
    abstract void dispose();
}
